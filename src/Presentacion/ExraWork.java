    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    
            int id = combo.getSelectedIndex();
                if (id==0||txtCantidad.getText().isEmpty()||txtDetalles.getText().isEmpty()||txtFecha.getDate()==null||txtPeriodo.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Empty Fields");}
                else{
            System.out.println(id);
            AltaChofer prov = new AltaChofer();
            prov.setId(id);
            (new puenteAltas()).PuenteBuscarChofer2(prov);
            String Nombre = prov.getNombre();
            String Apellidos = prov.getApellidos();
            System.out.println(Nombre + " " + Apellidos);

            AltaExtraWork E = new AltaExtraWork();
           // E.setId(id);
            E.setFirst_Name(Nombre);
            E.setLast_Name(Apellidos);
            E.setPeriod(Integer.parseInt(txtPeriodo.getText()));
            E.setYear(Integer.parseInt(año.getSelectedItem().toString()));

            java.util.Date fecha = txtFecha.getDate();
            long d = fecha.getTime();
            java.sql.Date soloFecha = new java.sql.Date(d);
            E.setDate(soloFecha);
            E.setDetails(txtDetalles.getText());
            E.setAmount(Float.parseFloat(txtCantidad.getText()));
            
            Metodos_Altas me=new Metodos_Altas();
            me.GuardarTrabajoExtra(E);
            txtCantidad.setText("");
            txtDetalles.setText("");
            txtFecha.setDate(null);
            txtPeriodo.setText("");
                }
    }//GEN-LAST:event_jButton2ActionPerformed

